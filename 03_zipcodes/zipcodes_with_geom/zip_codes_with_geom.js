var fs = require('fs');
var d3 = require('d3');

DEBUG = true;

// remove_before_execute(['states.geojson']);

fs.readFile('zip_code_database.csv', 'utf8', function (err, raw_zip) {
	out_data = d3.csv.parse(raw_zip, function (c) {
		var state = state_abbr_name[c.state];
		if (state) {
			console.log(c);
			var feature = {};
			feature.properties = {
				'state': state,
				'state_abbr': c.state,
				'zip': c.zip,
				'zip_lat': c.latitude,
				'zip_lon': c.longitude
			};
			// feature.geometry = geometry
			return feature;
		}
		return {};
	});

	fs.writeFileSync('zip_codes_by_state.geojson',
					JSON.stringify(out_data));

});